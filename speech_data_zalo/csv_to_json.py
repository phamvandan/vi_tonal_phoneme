def get_duration_of_audio_file(file_name):
    duration = 0
    with contextlib.closing(wave.open(file_name, 'r')) as f:
        frames = f.getnframes()
        rate = f.getframerate()
        duration = frames / float(rate)
    duration = round(duration, 2)
    return duration

